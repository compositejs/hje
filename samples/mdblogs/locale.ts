    const obj = {
        strings: en,
        "strings#en": en,
        "strings#zh-Hans": hans,
        "strings#zh-CN": hans,
        "strings#zh-SG": hans,
    }

    export function getLocaleString(key: keyof typeof en, mkt?: string | boolean | undefined) {
        const res = getLocaleProp(obj, "strings", { mkt });
        return res[key] || obj.strings[key];
    }

    export function setElementText(element: HTMLElement, key: keyof typeof en) {
        if (!key || typeof key !== "string") return undefined;
        const s = getLocaleString(key);
        if (!s) return s;
        if (element) {
            if (typeof element === "string") element = document.getElementById(element);
            if (element && element.tagName) element.innerText = s;
        }

        return s;
    };
